/**BOOLEANS
 * El constructor del boolean permite capturar otro tipo de datos como true o false
 */


let verdadero = true
let falso = false

console.log(verdadero)
console.log(falso)

console.log(typeof verdadero, typeof falso)
console.log(Boolean(0),'cero')//vale false
console.log(Boolean(1),'1')//true
console.log(Boolean(-10),'-10')//true
console.log(Boolean(""),"")//false
console.log(Boolean(" "),' ')//true
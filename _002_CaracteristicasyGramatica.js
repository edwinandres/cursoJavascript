/* CARACTERISTICAS
-JS Tiene Isomorfismo = misma morma
lo cual quiere decir que puedes hacer las 3 capas de una aplicacion con JS
FrontEnd BackEnd y Persistencia de datos
-JS es el unico lenguaje del lado del front, cualquier otro lenguaje que uses para eso
finalmente tendras que compilarlo a JS
-En el backend se usa NodeJS
-Con JS puedes hacer de todo(manejar drones, machine learning, etc)
-Lenguaje de alto nivel (recoleccion de basura, etc...)
-Interpretado y dinamico( no necesita compilacion, se compila en tiempo de ejecucion)
-Debilmente tipado(una variable puede cambiar de tipo a lo largo del tiempo)
-Multiparadigma(programacion funcional y otras)
-Case sensitive (var Perro y var perro son 2 variables distintas)
-No es necesario finalizar con punto y coma cada sentencia

*/

/*ESCRITURA DE CODIGO
-Identificadores deben comenzar con letra signo de dolar o subguion(jamas numeros o caracteres especiales)
-archivos => snake_case = mi_archivo (en react y angular no aplica)
-constantes => UPPER_CASE = UNA_CONSTANTE
-clases => UpperCamelCase = class SerHumano
-objetos,valores primitivos , funciones,instancias => lowerCamelCase = unObjeto


*/

